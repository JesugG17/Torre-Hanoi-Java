package HANOIMVC;

public class Torre {

    private int posX, discos;

    public Torre(int posX) {
        this.posX = posX;
    }

    public void setDiscos(int discos) {
        this.discos = discos;
    }

    public int getDiscos() {
        return discos;
    }
    public int getPosX() {
        return posX;
    }


}
